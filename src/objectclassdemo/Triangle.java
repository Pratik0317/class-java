package objectclassdemo;

public class Triangle {

	private float b;
	private float h;
	private float p;
	
	public Triangle() {
		
	}
	
	public Triangle(float b, float h, float p) {
		this.b = b;
		this.h = h;
		this.p = p;
	}

	public float getB() {
		return b;
	}
	public void setB(float b) {
		this.b = b;
	}
	public float getH() {
		return h;
	}
	public void setH(float h) {
		this.h = h;
	}
	public float getP() {
		return p;
	}
	public void setP(float p) {
		this.p = p;
	}
	
	
}
